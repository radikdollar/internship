def main():
    # Читаем количество строк
    n = int(input())

    # Считываем пирамиду
    triangle = []
    for _ in range(n):
        # Считываем строку, разбиваем по пробелам и переводим в integers
        row = list(map(int, input().split()))
        triangle.append(row)

    # Создаем копию треугольника (матрицу dp) для хранения минимальных сумм маршрутов
    dp = []
    for row in triangle:
        dp.append(row[:])

    # 1 ЭТАП: Идем снизу вверх, начиная с предпоследней строки к вершине
    for i in range(n - 2, -1, -1):
        for j in range(len(dp[i])):
            # К текущей ячейке прибавляем минимум из двух доступных ячеек снизу
            dp[i][j] += min(dp[i + 1][j], dp[i + 1][j + 1])

    # Минимальная сумма всего пути окажется на самой вершине пирамиды
    min_sum = dp[0][0]

    # 2 ЭТАП: Восстанавливаем маршрут, спускаясь сверху вниз
    path = [triangle[0][0]]
    current_j = 0  # начинаем с вершины 0

    for i in range(n - 1):
        # Смотрим, куда было выгоднее пойти на шаге ниже
        if dp[i + 1][current_j] < dp[i + 1][current_j + 1]:
            # Выгоднее было пойти влево-вниз
            path.append(triangle[i + 1][current_j])
        else:
            # Выгоднее было пойти вправо-вниз
            path.append(triangle[i + 1][current_j + 1])
            current_j += 1  # смещаем индекс

    # Выводим результат
    print(min_sum)
    # Распаковываем список пути и выводим через пробел
    print(*path)

if __name__ == "__main__":
    main()

