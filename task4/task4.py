def main():
    # n это длина слова, k это сколько раз его зашифровали
    n, k = map(int, input().split())

    # p это список правил перестановки
    p = list(map(int, input().split()))

    # word это зашифрованное слово (strip убирает лишние пробелы по краям если они есть)
    word = input().strip()

    # Делаем из строки список букв, чтобы их можно было менять местами
    current_word = list(word)

    # 2. Делаем расшифровку k раз
    for _ in range(k):
        # Пустой список для слова из предыдущего шага
        prev_word = [''] * n

        # Проходим по всем буквам
        for i in range(n):
            # При шифровании букву брали с позиции p[i] и ставили на позицию i
            original_index = p[i] - 1
            prev_word[original_index] = current_word[i]

        # Обновляем текущее слово для следующего шага цикла
        current_word = prev_word

    # 3. Склеиваем список букв обратно в строку и печатаем
    print("".join(current_word))


if __name__ == "__main__":
    main()
