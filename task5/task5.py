def main():
    # 1. Считываем высоту (h) и ширину (w) матрицы
    h, w = map(int, input().split())

    # Задаем начальные значения для поиска крайних точек.
    # Минимумы делаем большими, а максимумы маленькими.
    min_r = h
    max_r = -1
    min_c = w
    max_c = -1

    # 2. Читаем матрицу построчно
    for i in range(h):
        # Читаем строку и превращаем ее в список чисел
        row = list(map(int, input().split()))

        # Проходим по каждому элементу в строке (j - это номер столбца)
        for j in range(w):
            if row[j] == 1:
                # Если нашли единицу, обновляем крайние границы
                if i < min_r:
                    min_r = i
                if i > max_r:
                    max_r = i
                if j < min_c:
                    min_c = j
                if j > max_c:
                    max_c = j

    # 3. Выводим результат.
    # По условию рамка не должна касаться фигуры, поэтому:
    # от минимумов отнимаем 1 (сдвигаем вверх и влево)
    # к максимумам прибавляем 1 (сдвигаем вниз и вправо)
    print(min_r - 1, min_c - 1, max_r + 1, max_c + 1)

if __name__ == "__main__":
    main()
